class Solution {
    public List<List<Integer>> generate(int numRows) {
        List<List<Integer>> result = new ArrayList<>();

        for (int i = 0; i < numRows; i++) {
            List<Integer> list = new ArrayList<>();
            list.add(1);
            for (int j = 1; j < i + 1 ; j++) {
                list.add( result.get(i-1).get(j-1) + (j == i ? 0 : result.get(i-1).get(j)));
            }
            result.add(list);
        }
        return result;
    }
}
