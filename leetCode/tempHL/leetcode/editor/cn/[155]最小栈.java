class MinStack {
    //			解答成功:
    //			执行耗时:9 ms,击败了17.13% 的Java用户
    //			内存消耗:40.1 MB,击败了72.52% 的Java用户
    //处理方法： 一个栈存数字，一个栈存当前栈的最小值
    /** initialize your data structure here. */
    Stack<Integer> stack ;
    Stack<Integer> minStack;
    public MinStack() {
        stack = new Stack();
        minStack = new Stack();
    }
    
    public void push(int x) {
        minStack.push( Math.min( minStack.isEmpty() ? Integer.MAX_VALUE : minStack.peek(), x));
        stack.push(x);
    }
    
    public void pop() {
        minStack.pop();
        stack.pop();
    }
    
    public int top() {
        return stack.peek();
    }
    
    public int getMin() {
        return minStack.peek();
    }
}

